#Concatenacion con espacios.

nombre="Juan"
apellido="Perez"

nombre_completo=(nombre+" "+apellido)

print(nombre_completo)

#Output "Juan Perez"

#----------------------------------------------------------------------------------------------

#Concatenacion de varias cadenas.

frase1="El clima en"
