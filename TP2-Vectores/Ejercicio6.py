#Dada una lista de palabras, convertirlas todas en mayúsculas.
lista_palabras = ["hola", "mundo", "python", "programacion"]
lista_mayusculas = []
for palabra in lista_palabras:
    lista_mayusculas.append(palabra.upper())

# Imprimir la lista de palabras en mayúsculas
print(f"Lista de palabras en mayúsculas: {lista_mayusculas}")
